public class Audio extends Media {
    private int duration;
    private String artist;

    public Audio() {
        this.duration = duration;
        this.artist = artist;
    }

    public int getDuration() {
        return duration;
    }

    public getArtist() {
        return artist;
    }

    private int setDuration() {
        return this.duration;
    }

    private String setArtist() {
        return this.Artist;
    }
}
